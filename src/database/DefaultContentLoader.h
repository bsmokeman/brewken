#ifndef DATABASE_DEFAULTCONTENTLOADER_H
#define DATABASE_DEFAULTCONTENTLOADER_H
#pragma once

class QSqlDatabase;
class QTextStream;

namespace DefaultContentLoader {
   /**
    * \brief Number of default content files available to load in. Increment every time we add a new default contents
    *        file (eg for new styles, ingredients, sample recipes, etc).
    */
   extern int const availableContentVersion;

   enum class UpdateResult {
      Failed     ,
      Succeeded  ,
      NothingToDo,
   };

   /**
    * \brief Populates (or updates) default Recipes, Hops, Styles, etc in the DB
    */
   UpdateResult updateContentIfNecessary(QSqlDatabase & db, QTextStream & userMessage);
}
#endif
